import * as React from "react"

import { cn } from "@/lib/utils"

function Card({
  className,
  size = "default",
  ...props
}: React.ComponentProps<"div"> & { size?: "default" | "sm" | "lg" }) {
  return (
    <div
      data-slot="card"
      data-size={size}
      className={cn(
        "group/card flex flex-col gap-(--card-spacing) overflow-hidden rounded-[var(--radius-card)] border border-line bg-surface text-ink shadow-none [--card-spacing:--spacing(5)] data-[size=lg]:rounded-[var(--radius-card-lg)] data-[size=sm]:[--card-spacing:--spacing(4)] data-[size=sm]:rounded-[var(--radius-card)]",
        className
      )}
      {...props}
    />
  )
}

